def cariJodohMandangTinggi(tinggiCewe,tinggiCowo):
    if tinggiCowo > tinggiCewe:
        print("Kamu tipe aku banget deh <3")
    else:
        print( "Maaf mungkin kita lebih baik temenean aja :)")

inp_tinggiCewe = int(input("Tinggi Nadia adalah : "))
inp_tinggiCowo = int(input("Tinggi Calon Jodoh adalah : "))
cariJodohMandangTinggi(inp_tinggiCewe,inp_tinggiCowo)
